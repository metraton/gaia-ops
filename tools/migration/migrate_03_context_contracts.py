#!/usr/bin/env python3
"""
migrate_03_context_contracts.py

Convierte project-context.json -> archivo SQL con INSERT OR IGNORE.

Reglas:
  - Solo I/O sobre filesystem.
  - NO importa sqlite3.
  - Idempotente: PK = (project, section_name).
  - Cada sección de top-level "sections" es una fila; payload = json.dumps(value).
  - metadata = json.dumps(top-level metadata) -- replicado en cada fila.

CLI args (parametrización cross-workspace):
  --project   workspace name (default: 'me')
  --src       path al project-context.json (default: ws/me)
  --out       path al SQL de salida (default: /tmp/migrate_03_context_contracts.sql)
  --fragment  emite solo INSERTs (sin BEGIN/COMMIT)
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

DEFAULT_PROJECT = "me"
DEFAULT_SRC = Path("/home/jorge/ws/me/.claude/project-context/project-context.json")
DEFAULT_OUT = Path("/tmp/migrate_03_context_contracts.sql")

COLUMNS = ["project", "section_name", "payload", "metadata", "updated_at"]


def sql_quote(value) -> str:
    if value is None:
        return "NULL"
    s = str(value)
    return "'" + s.replace("'", "''") + "'"


def row_values_sql(row: dict) -> str:
    return "(" + ",".join(sql_quote(row.get(col)) for col in COLUMNS) + ")"


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate INSERT SQL for context_contracts table.")
    parser.add_argument("--project", default=DEFAULT_PROJECT)
    parser.add_argument("--src", default=str(DEFAULT_SRC), help="path to project-context.json")
    parser.add_argument("--out", default=str(DEFAULT_OUT))
    parser.add_argument("--fragment", action="store_true")
    args = parser.parse_args()

    project = args.project
    src = Path(args.src)
    out = Path(args.out)
    fragment = args.fragment

    if not src.exists():
        print(f"[migrate_03:{project}] ERROR: source not found: {src}", file=sys.stderr)
        return 1

    data = json.loads(src.read_text(encoding="utf-8"))
    metadata = data.get("metadata") or {}
    sections = data.get("sections") or {}

    metadata_text = json.dumps(metadata, ensure_ascii=False, separators=(",", ":"))
    updated_at = metadata.get("last_updated")

    rows = []
    for section_name, payload in sections.items():
        rows.append(
            {
                "project": project,
                "section_name": section_name,
                "payload": json.dumps(payload, ensure_ascii=False, separators=(",", ":")),
                "metadata": metadata_text,
                "updated_at": updated_at,
            }
        )

    cols_csv = ",".join(COLUMNS)
    insert_prefix = f"INSERT OR IGNORE INTO context_contracts ({cols_csv}) VALUES\n"

    with out.open("w", encoding="utf-8") as fh:
        fh.write(f"-- Generated by migrate_03_context_contracts.py (idempotent)\n")
        fh.write(f"-- Project: {project}\n")
        fh.write(f"-- Source: {src}\n")
        fh.write(f"-- Sections to insert: {len(rows)}\n")
        if not fragment:
            fh.write("BEGIN TRANSACTION;\n")
        if rows:
            fh.write(insert_prefix)
            fh.write(",\n".join(row_values_sql(r) for r in rows))
            fh.write(";\n")
        if not fragment:
            fh.write("COMMIT;\n")

    print(f"[migrate_03:{project}] wrote {out} ({len(rows)} rows)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
